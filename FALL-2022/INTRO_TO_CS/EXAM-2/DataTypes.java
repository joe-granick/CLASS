public class DataTypes
{
    public static void main(String[] args)
    {
    System.out.println("System.out.println((3 + 4.5))                   :  " + (3+4.5));
    System.out.println("System.out.println( 3 + 4.5 )                   :  " + 3+4.5);
    System.out.println("Math.pow(5/2,2)                                :  " + Math.pow(5/2,2));
    System.out.println("Math.pow(5.0/2,2)                              :  " + Math.pow(5.0/2,2));
    System.out.println("Math.min(2,4))                                 :  " + Math.min(2,4));
    System.out.println("Math.min(2,4))                                 :  " + Math.min(2.0,4.0));
    System.out.println("Math.ceil(5.2)                                 :  " + Math.ceil(5.2));
    System.out.println("Math.max(19-5+Math.random(), Math.ceil(5.1*3))): " + Math.max(19-5+Math.random(), Math.ceil(5.1*3)));
    
    }
}